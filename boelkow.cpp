uint64_t boelkow(const void* eingabe, size_t laenge)
{
    const uint8_t* eingabeOktets = (const uint8_t*)eingabe;
    uint64_t zustand = 0x1234567812345678ll;
    
    for(size_t stelle = 0; stelle < laenge; stelle++)
    {
       uint64_t* piFunc = (uint64_t*) &pi264[eingabeOktets[stelle]];      
       zustand += *piFunc;
       zustand = rotiere(zustand,3);
    }
    return zustand;
}




